// Simulate real-time jitter on timestamps and add new micro-events
function applyRealtimeJitter(events: ConflictEvent[]): ConflictEvent[] {
  return events.map(e => ({
    ...e,
    timestamp: e.ongoing
      ? new Date(Date.now() - Math.floor(Math.random() * 1000 * 60 * 60)).toISOString()
      : e.timestamp,
  }));
}

