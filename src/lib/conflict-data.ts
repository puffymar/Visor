export function getIncrementalEvents(): ConflictEvent[] {
  const epoch = new Date().setHours(0, 0, 0, 0);
  const batch = Math.floor((Date.now() - epoch) / INCREMENTAL_BATCH_MS);
  const count = Math.min(5 * (batch + 1), INCREMENTAL_POOL.length);
  return INCREMENTAL_POOL.slice(0, count).map((e, i) => ({
    ...e,
    timestamp: new Date(Date.now() - (count - i) * 60 * 1000).toISOString(),
  }));
}

